    """在普通案例上追加判断性编码（保留全部原始字段）。"""
    e = dict(c)
    e["基本案情摘要"] = summary
    e["抗辩要点摘要"] = defense_pts
    e["裁判要点摘要"] = ruling_pts
    e["判赔金额"] = amount
    e["维权开支"] = None
    e["裁判结果分类"] = result_cls
    e["焦点立场"] = focus_pos
    e["抗辩"] = defenses
    e["相关度"] = "高"
    return e


